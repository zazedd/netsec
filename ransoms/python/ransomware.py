def loadKey():
    if getattr(sys, 'frozen', False):
        keyPath = os.path.join(sys._MEIPASS, "secretKey.key")
    else:
        keyPath = "secretKey.key"
    return open(keyPath, "rb").read()

def encryptFolderAsZip(folderPath):

    zipFileName = f"{folderPath}.zip"
    
    with zipfile.ZipFile(zipFileName, 'w', zipfile.ZIP_DEFLATED) as zipf:
        for root, dirs, files in os.walk(folderPath):
            for file in files:
                filePath = os.path.join(root, file)
                arcName = os.path.relpath(filePath, folderPath)
                zipf.write(filePath, arcName)
    
    key = loadKey()
    fernet = Fernet(key)
    with open(zipFileName, "rb") as f:
        zipData = f.read()
    encryptedZipData = fernet.encrypt(zipData)
    
    with open(zipFileName, "wb") as f:
        f.write(encryptedZipData)

    shutil.rmtree(folderPath)
       


